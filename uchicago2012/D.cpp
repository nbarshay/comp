typedef long long ll;

#define TWO(x) (1LL<<(x))

int main(){
	while(1){
		string s; cin>>s;
		if(s == "X")
			break;
		ll res = 0;
		bool last;
		for(ll i = sz(s)-1; i >= 0; i--){
			if(i == sz(s)-1){
				if(s[i] == 'A'){
					last = false;
				} else if(s[i]== 'B'){
					last = true;
				} else{
					assert(false);
				}
			} else{
				if(s[i] != s[i+1]){
					last = !last;
				}
			}
			if(last)
				res |= TWO(i);
		}
		cout << TWO(sz(s)) - 1 - res << endl;
	}
}
